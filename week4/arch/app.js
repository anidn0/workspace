


const choice = prompt("Select a size");

switch (choice) {
 case "small":
 console.log("You have ordered a small shirt");
 break;
 case "medium":
 console.log("You have ordered a medium shirt");
 break;
 case "large":
 console.log("You have ordered a large shirt");
 break;
 default:
 console.log("We have no other sizes");
 break;
}
